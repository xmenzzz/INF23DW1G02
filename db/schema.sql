DROP SCHEMA IF EXISTS 'temporada';

create schema 'temporada';
use 'temporada';

create table temporada(
"id" int auto_increment primary key,
"nome" varchar(50),
"equipa_id" int,
foreign key ('equipa_id') references 'equipa'('id')
);

create table equipa(
'id' int auto_increment primary key,
'nome' varchar(50));

create table estatisticas (
'id' int primary key ,
'golos_feitas' int ,
'golos_sofridos' int,
'faltas_sofridas' int,
'faltas_feitas' int,
'temporada_id' int references 'temporada'('id'),
'id_equipa' int references 'equipa'('id')
);

create table jogador (
'id' int auto_increment primary key ,
'nome' nvarchar (50),
'numero' int,
'temporada_id' int references 'temporada'('id'),
'id_plantel' int references 'plantel'('id'),
'id_equipa' int references 'equipa'('id')
);

create table jogos (
'id' int auto_increment primary key ,
'resultado' varchar (100),
'temporada_id' int references 'temporada'('id'),
'equipa_id' int,
foreign key ('equipa_id') references 'equipa'('id')
);

create table ocurrencia(
'id' int primary key  ,
'num_faltas' int,
'num_golos' int,
'num_cartao' int,
'minutos' float ,
'substituicao' int,
'temporada_id' int references 'temporada'('id'),
'id_equipa' int references 'equipa'('id'),
'id_jogador' int references 'jogador'('id'),
'id_jogos' int references 'jogos'('id')
);











